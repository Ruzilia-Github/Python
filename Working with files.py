location = "/Users/ruzilia/Desktop/lines.txt"
with open(location, encoding='utf-8') as file:
    letters = 0
    words = 0
    lines = 0
    for str in file:
        lines +=1
        for letter in str:
            if letter.isalpha():
                letters +=1
        for letter in str.split():
            words +=1
